const LangContext = createContext();

export function LanguageProvider({ children, defaultLang = 'fr' }) {
  const [lang, setLang] = useState(() => {
    return localStorage.getItem('multiwave_lang') || defaultLang;
  });

  const changeLang = (newLang) => {
    setLang(newLang);
    localStorage.setItem('multiwave_lang', newLang);
    document.dir = ['ar', 'fa'].includes(newLang) ? 'rtl' : 'ltr';
  };

  return (
    <LangContext.Provider value={{ lang, changeLang }}>
      {children}
    </LangContext.Provider>
  );
}

export function useTranslation() {
  const ctx = useContext(LangContext);
  if (!ctx) return { t: (k) => k, lang: 'fr', changeLang: () => {}, languages: LANGUAGES };
  const { lang, changeLang } = ctx;
  const t = (key) => T[lang]?.[key] || T['fr']?.[key] || key;
  return { t, lang, changeLang, languages: LANGUAGES };
}

export default T;